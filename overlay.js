function showDiv()
{
	document.getElementById("cover").style.display = 'block';
	document.getElementById("loginScreen").style.display='block';
	document.getElementById("nameTitle").style.display='block';
	document.getElementById("descriptionOfEvent").style.display='block';
	document.getElementById("image").style.display='block';
	document.getElementById("add").style.display='block';

}

function cancel()
{
	document.getElementById("cover").style.display = 'none';
	document.getElementById("loginScreen").style.display= 'none';
	document.getElementById("nameTitle").style.display= 'none';
	document.getElementById("descriptionOfEvent").style.display= 'none';
	document.getElementById("image").style.display= 'none';
	document.getElementById("add").style.display= 'none';

}
